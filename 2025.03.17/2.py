n1 = int(input())
n2 = int(input())

if n1 % n2 == 0:
    n_1 = n1 / n2
    n_1 = int(n_1)
    print(f'{n1} делится на {n2} нацело\nчастное: {n_1}')

else:
    n2 = n1 // n2
    n3 = n1 % n2
    print(f'{n1} не делится на {n2} нацело \nнеполное частное: {n2}\nостаток: {n3}')


